package day03_methodOlusturmaVeKullanma;

public class C01_NicinMethodKullaniriz {

    public static void main(String[] args) {

        // sinirlar dahil olmak uzere
        // 10 ile 100 arasindaki tamsayilarin toplamini yazdirin

        int toplam = 0;

        for (int i = 10; i <=100 ; i++) {
            toplam += i;
        }

        System.out.println("Toplam isleminin sonucu : " + toplam);

        // sinirlar dahil olmak uzere
        // 37 ile 141 arasindaki tamsayilarin toplamini yazdirin

        toplam = 0;

        for (int i = 37; i <=141 ; i++) {
            toplam+=i;
        }
        System.out.println("Toplam isleminin sonucu : " + toplam);




    }
}
